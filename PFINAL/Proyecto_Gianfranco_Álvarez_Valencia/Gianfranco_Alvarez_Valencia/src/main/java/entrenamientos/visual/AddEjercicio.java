    /** Funcion para comprobar que un striung es valdio como número
     * @param args the command line arguments
     */
    
    public static boolean isNumeric(String s)
    {
        if (s == null || s.equals("")) {
            return false;
        }
 
        return s.chars().allMatch(Character::isDigit);
    }
    
