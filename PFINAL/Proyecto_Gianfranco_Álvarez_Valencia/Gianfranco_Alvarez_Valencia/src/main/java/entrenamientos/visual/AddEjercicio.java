    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!(jTextFieldNombre.getText().toString().equals("") ||  jTextFieldSeries.getText().toString().equals("") || jTextFieldRepeticiones.getText().toString().equals("") || jTextFieldPeso.getText().toString().equals("")))
        {
            if(!(isNumeric(jTextFieldSeries.getText())))
                JOptionPane.showMessageDialog(null, "El campo series tiene que ser un numero", "", JOptionPane.ERROR_MESSAGE);
            else 
                if  (!(isNumeric(jTextFieldRepeticiones.getText())))
                    JOptionPane.showMessageDialog(null, "El campo repeticiones tiene que ser un numero", "", JOptionPane.ERROR_MESSAGE);
                else
                    if (!(isNumeric(jTextFieldPeso.getText())))
                        JOptionPane.showMessageDialog(null, "El campo repeticiones tiene que ser un numero", "", JOptionPane.ERROR_MESSAGE);
                    else
                    {
                        AccesoDatosSql.addEjercicio(jTextFieldNombre.getText().toString(),codigoRutina, jComboBoxGmuscular.getSelectedItem().toString(),jTextFieldSeries.getText().toString(),jTextFieldRepeticiones.getText().toString(),jTextFieldPeso.getText().toString());
                        parent.actualizarTablaEjericios(codigoRutina);
                        dispose();
                    }
        }
        else
            JOptionPane.showMessageDialog(null, "Hay que relleanr todos los campos", "", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed
