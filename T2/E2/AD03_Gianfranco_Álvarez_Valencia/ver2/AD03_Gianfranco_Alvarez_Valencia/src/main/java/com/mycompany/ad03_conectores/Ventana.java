    private void nuevoPasajeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nuevoPasajeroActionPerformed
        int fila = tablaVuelos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un vuelo para aÃ±adir un pasajero.");
        } else {
            //ABRIR VENTANA  Y RECOGER DATOS
            VentanaPasajero ventanaCrear = new VentanaPasajero(this, VentanaPasajero.MODO_CREAR);
            ventanaCrear.setVisible(rootPaneCheckingEnabled);
            ventanaCrear.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);   
       }
    }//GEN-LAST:event_nuevoPasajeroActionPerformed

    private void eliiminarPasajeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliiminarPasajeroActionPerformed
        int fila = tablaPasajeros.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un pasajero para eliminar.");
        } else {
            String dniPasajero = tablaPasajeros.getModel().getValueAt(fila, 0).toString();
            int confirmacion = JOptionPane.showConfirmDialog(this, "Â¿EstÃ¡ seguro que desea eliminar el pasajero con dni " + dniPasajero + "?");
            if (confirmacion == 0) {
                String mensaje = AccesoDatos.borrarPasajero(codigoVuelo, dniPasajero);
                JOptionPane.showMessageDialog(null, mensaje);
                   
                actualizarTablaPasajeros();
            }
        }
    }//GEN-LAST:event_eliiminarPasajeroActionPerformed

    private void cambiarAsientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cambiarAsientoActionPerformed
        int fila = tablaPasajeros.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un pasajero para cambiar el asiento.");
        } else {
            //ABRIR VENTANA  Y RECOGER DATOS
            VentanaPasajero ventanaEditar = new VentanaPasajero(this, VentanaPasajero.MODO_EDITAR);
            ventanaEditar.setCampoDNI(tablaPasajeros.getModel().getValueAt(fila, 0).toString());
            ventanaEditar.setCampoTipoPlaza(tablaPasajeros.getModel().getValueAt(fila, 1).toString());
            ventanaEditar.setCampoAsiento(tablaPasajeros.getModel().getValueAt(fila, 2).toString());
            ventanaEditar.setVisible(rootPaneCheckingEnabled);
            ventanaEditar.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);   
            
        }
    }//GEN-LAST:event_cambiarAsientoActionPerformed

    private void tablaVuelosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaVuelosMouseClicked
        int fila = tablaVuelos.getSelectedRow();
        codigoVuelo = tablaVuelos.getModel().getValueAt(fila, 0).toString();
        actualizarTablaPasajeros();
    }//GEN-LAST:event_tablaVuelosMouseClicked

    
    public void actualizarTablaPasajeros() {
         
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("DNI");
        tableHeaders.add("TIPO");
        tableHeaders.add("PLAZA");
        
        ArrayList<Pasajero> pasajeros = AccesoDatos.buscarPasajeros(codigoVuelo);
        
        for (Pasajero pasajero : pasajeros) {
            Vector<Object> oneRow = new Vector<Object>();
            
            oneRow.add(pasajero.getDni());
            oneRow.add(pasajero.getTipo_plaza());
            oneRow.add(pasajero.getPlaza());
            
            tableData.add(oneRow);
        }
        
        tablaPasajeros.setModel(new DefaultTableModel(tableData, tableHeaders));
        
        
    }
    
    
